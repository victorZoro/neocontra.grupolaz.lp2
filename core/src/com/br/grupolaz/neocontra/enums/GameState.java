/**
 * <h2>GameState</h2>
 * <p>A enumeração GameState é define os
 *  possíveis estados do jogo.
 * <p> Ela forneceuma maneira de representar 
 * e distinguirentre os diferentes estados em
 *  que o jogo pode estar, como "em execução", 
 * "pausado" ou "terminado". </P>
 * 
 * <h3>package</h3>
 * <p>enums</p>
 * 
 * <h3>Estados presentes na enumeração</h3>
 * <P> RUNNING</p>
 * <p>PAUSED</p>
 * <p>OVER</p>
 */
public enum GameState {
    RUNNING,
    PAUSED,
    OVER
}
